
CREATE TABLE PUBLIC.CHARACTERS (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1) PRIMARY KEY , 
  name VARCHAR(300),
  description VARCHAR(30000),
  last_update DATE
);

CREATE TABLE PUBLIC.COMICS (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1) PRIMARY KEY , 
  title VARCHAR(300),
  issue  int
);

CREATE TABLE PUBLIC.COMICS_CHARACTERS (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1) PRIMARY KEY , 
  id_character int,
  id_comic int
);

ALTER TABLE COMICS_CHARACTERS 
ADD FOREIGN KEY (id_character) REFERENCES CHARACTERS (id) 

ALTER TABLE COMICS_CHARACTERS 
ADD FOREIGN KEY (id_comic) REFERENCES COMICS (id) 


